$("#m1").keyup(function () { 
    var m1=$("#m1").val();
    if(m1 == "")
    {
        m1=0;
    }
    if(parseFloat(m1)>=11)
    {
    $(".form-m1").html("Enter Maximum 10 Mark !!");
    }
    else
    {
    $(".form-m1").html("");  
    }
});
