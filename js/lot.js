$("#lotnumber").keyup(function(){
    var lot=$("#lotnumber").val();
    if(parseInt(lot)>100)
    {
    $(".form-lot").html("Invaild Lotnumber");
    }
    else
    {
    $(".form-lot").html("");   
    }
});

